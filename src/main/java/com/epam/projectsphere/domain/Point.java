package com.epam.projectsphere.domain;

public class Point {
		
	private double abscissa;
	private double ordinate;
		
	public void setAbscissa(double abscissaPoint) { 
		this.abscissa = abscissaPoint;
	}
		 
	public double getAbscissa() { 
		return abscissa;
	}
		 
	public void setOrdinate(double ordinatePoint) { 
		this.ordinate = ordinatePoint;
	} 
		 
	public double getOrdinate() { 
		return ordinate;
	}
}

